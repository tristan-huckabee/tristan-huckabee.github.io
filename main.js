document.getElementsByClassName("fa-times-circle")[0].addEventListener("click", closeOne);
one.addEventListener("click", popOne);
function popOne(){
    let obj = document.getElementsByClassName("popUp")[0];
    
    if(obj.style.visibility == "hidden"){
        obj.style.visibility = "visible";
    }
}
